#include "libft.h"
#include <stdio.h>

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (lst == NULL)
		return ;
	del(lst->key);
	if (lst->value)
		del(lst->value);
	free(lst);
}
